<div class="content">
	<h1>How to Play </h1>
	<p>
		<h1>Général: </h1>
		Galaxie divisée en 8 zones en triangle qui forment un cercle<br/>
		Dans chaque zone habitable au centre une planète désignée comme capitale, a autorité sur les planètes de la zone<br/>
		Au centre de la galaxie, une planète a autorité sur les capitales<br/>
		<br/>
		Chaque planète capitale touche une part sur tous les achats effectués dans la zone<br/>
		La planète centrale touche une part sur toutes les parts obtenues par les capitales<br/>
		Au départ les 8 capitales et la responsable sont controlées par le serveur, avec les taxes à 0%<br/>
		<br/>
		Chaque zone dispose de maximum 1000 systèmes solaires générés aléatoirement en position et contenant des ressources aléatoires (mais équilibrées)<br/>
		Le joueur au spawn est placé sur une planète dans un système solaire vide dans la zone à équilibrer (voir plus bas)<br/>
		<br/>
		<h1>Batiments: </h1>
		&nbsp;&nbsp;Command Room - Permet de voir les expéditions en cours, le status de la planète etc.<br/>
		&nbsp;&nbsp;Scanner - Permet de scanner les planètes et les champs d'astéroides aux alentours. Les infos apparaissent alors sur la map.<br/>
		&nbsp;&nbsp;Marché - Peut ensuite mettre en vente des objets vaisseaux et en acheter. Ils sont alors livrés/emportés par la planète mère et mis sur le marché global.<br/>
		&nbsp;&nbsp;Hangar - Permet de garer des vaisseaux et d'en recevoir d'autres joueurs. Sécurité paramètrable.<br/>
		&nbsp;&nbsp;Usine - Permet de construire les vaisseaux / armes / défenses.<br/>
		&nbsp;&nbsp;Laboratoire - Permet de chercher des améliorations pour les bâtiments.<br/>
		&nbsp;&nbsp;Bibliothèque - Permet d'entrainer ses skills et d'en apprendre de nouveaux. Chaque skill necessite un skillbook achetable.<br/>
		<br/>
		Un vaisseau necessite des ressources pour être construit, ainsi qu'une recette de fabrication et à un taux de succès. Le joueur peut acheter/mettre en vente son vaisseau sur le marché global.<br/>
		Un vaisseau possède des sockets pour se défendre/attaquer/stocker.<br/>
		<br/>
		Le joueur peut mettre en vente des objets dans son propre marché, qui n'est pas soumis aux frais de gestions de la mise en vente par le marché global.<br/>
		Pour acheter dans ces marchés privés les joueurs doivent aller parquer un vaisseau.<br/>
		<br/>
		<h1>Resources:</h1>
		&nbsp;&nbsp;Gallesium - Commun<br/>
		&nbsp;&nbsp;Aronine - Commun<br/>
		&nbsp;&nbsp;Solidium - Commun<br/>
		&nbsp;&nbsp;8 ressources de base propres à chaque zone<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Red Blue Yellow White Black Purple Green Gold Shards<br/>
		<br/>
		<br/>
		&nbsp;&nbsp;Generimese - Moyen<br/>
		&nbsp;&nbsp;Kelium - Moyen<br/>
		&nbsp;&nbsp;Nanonitrate - Moyen<br/>
		<br/>
		&nbsp;&nbsp;Minium - Rare<br/>
		&nbsp;&nbsp;Belsate - Rare<br/>
		<br/>
		<h1>Alliances:</h1>
		&nbsp;&nbsp;Un joueur peut monter une alliance et recruter n'importe quel autre joueur.<br/>
		&nbsp;&nbsp;Une alliance doit avoir une planète en guise de base.<br/>
		&nbsp;&nbsp;L'alliance peut acheter des skills qui profitent à tous les membres, contre des apports hebdomadaires en matériaux/crédits<br/>
		&nbsp;&nbsp;Skills:<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Attaque+ Defense+ Vitesse+ Récolte+ Recherche+<br/>
		<br/>
		<h1>Récolte:</h1>
		&nbsp;&nbsp;Pour récolter des ressources les joueurs doivent construire un vaisseau extracteur et l'envoyer sur une planète interessante ou un champ d'astéroides.<br/>
		&nbsp;&nbsp;Ils reviennent quand ils sont pleins ou qu'ils ont atteint une somme définie par le joueur.<br/>
		&nbsp;&nbsp;Les champs d'astéroides contiennent les matières moyennes et rares, mais ils ont une quantité limitée et disparaissent une fois vidés. Ils réapparaissent aléatoirement.<br/>
		<br/>
		<h1>Expedition:</h1>
		&nbsp;&nbsp;Envoyer son vaisseau sur une planète pour chercher des trésors. Peut trouver des reliques, recettes, skillbooks, matériaux rares, crédits, etc.<br/>
		<br/>
		<h1>Crafting:</h1>
		&nbsp;&nbsp;Crafts de vaisseaux, armes, armures, cargos avec les recettes trouvées et les matériaux disponibles. Crafts spécifiques à certaines zones necessitant des ressources spécifiques aux zones<br/>
		<br/>
		<h1>Combat:</h1>
		&nbsp;&nbsp;Le joueur peut placer ses vaisseaux en patrouille autour de n'importe quel objet, si un joueur ennemi entre dans le périmètre de patrouille, un combat se déclanche.<br/>
		&nbsp;&nbsp;Régulièrement, des vaisseaux aliens peuvent apparaitre à certains endroits et les joueurs peuvent les détruire pour obtenir des récompenses, recettes, crédits, skillbook, matériaux d'autres zones etc.<br/>
		&nbsp;&nbsp;Les vaisseaux aliens peuvent attaquer les bases et piller des ressources dans l'inventaire<br/>
		&nbsp;&nbsp;Les joueurs peuvent attaquer les bases et piller les joueurs qui sont dans des zones hostiles envers eux ou dans des alliances ennemies<br/>
		<br/>
		<h1>Politique:</h1>
		&nbsp;&nbsp;Chaque habitant possède 1 vote pour élire le représentant de la zone qui la contrôlera.<br/>
		&nbsp;&nbsp;Tout le monde peut s'inscrire sur la liste éléctorale et présenter son programme<br/>
		<br/>
		<h1>Sièges:</h1>
		&nbsp;&nbsp;Possible de prendre de force le contrôle de la zone tous les dimanches lors d'un siège de la planète, il faut avoir un vaisseau capitaine et détruire tous les autres assaillants. Si un seul vaisseau capitaine survit à la fin de la bataille, il obtient la planète. Sinon le précédent élu reprend sa place.<br/>
		&nbsp;&nbsp;Possible de prendre de force le contrôle du centre de la zone tous les samedis lors d'un siège de la planète mère. Mêmes règles.<br/>
		&nbsp;&nbsp;Une alliance doit s'inscrire à un siège pour prendre part<br/>
		&nbsp;&nbsp;Tout le monde peut participer aux sièges et choisir quel camp défendre: celui en place ou une alliance inscrite en attaque<br/>
		&nbsp;&nbsp;Celui en place dispose d'une armée de bots en plus qu'il peut upgrader pendant qu'il est au poste<br/>
		&nbsp;&nbsp;Un vaisseau spécial ne peut pas être envoyé après 1h de siège mais les renforts peuvent<br/>
		<br/>
		<h1>Avantages de contrôler une zone:</h1>
		&nbsp;&nbsp;L'élu fixe le taux de prélèvement sur les transactions qui sera appliqué dans la zone (maximum 21%)<br/>
		&nbsp;&nbsp;L'élu peut prendre dans la caisse pour ses affaires perso<br/>
		&nbsp;&nbsp;Peut upgrader les differents éléments de la zone et définir la politique de celle ci<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Hostile: Les habitants des autres zones ne sont pas les bienvenus et il est possible pour tout le monde de les attaquer<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Neutre: Les habitants des autres zones sont tolérés<br/>
		&nbsp;&nbsp;Améliorations des skills de zone: (sont remis à zéro tous les dimanches, chaque skill réduit le taux de prélèvement de 5%. Il faut minimum 5% pour activer un skill)<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Taux d'apparition des champs d'astéroides<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Réduction des temps de recherche et d'apprentissage de skills<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Buffs pour les habitants de la zone (attaque, défense, récolte)<br/>
		<br/>
		<h1>Avantages à contrôler la zone principale:</h1>
		&nbsp;&nbsp;Fixe le taux de prélèvement sur les zones (max 25%)<br/>
		&nbsp;&nbsp;Active un skill au choix pour tous les joueurs:<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Combat:<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Augmentation de l'attaque et de la défense de tous les vaisseaux<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Commerce:<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Réductions des frais liés au commerce global et amélioration des récoltes<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Passif:<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Rien<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Neutre:<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Augmentation de la vitesse des vaisseaux<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Tyranie:<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Augmentation de 10% des taux de prélèvement des zones (cumulatif)<br/>
		<br/>
		<h1>Zone 9: (zone circulaire autour des 8 zones habitées)</h1>
		&nbsp;&nbsp;Zone où tout le monde est considéré comme un ennemi sauf membres d'une même alliance<br/>
		&nbsp;&nbsp;Fort taux d'aliens<br/>
		&nbsp;&nbsp;Fort taux de champs d'astéroides dont "nombreux" rares et les 8 ressources de bases sont présentes<br/>
		&nbsp;&nbsp;Probabilité élevée de trésors sur les planètes, recettes de vaisseaux des anciens, skillbooks des anciens<br/>
		&nbsp;&nbsp;Seulement 80 systèmes solaires<br/>
		&nbsp;&nbsp;<br/>
		<h1>Equilibrage:</h1>
		&nbsp;&nbsp;Pour les zones en difficulté:<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;On améliore fait poper de nouveaux joueurs dans cette zone et le taux de rencontre de vaisseaux aliens des joueurs des autres zones augmentent sur ce territoire.<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;Les ressources rapportent moins pour les joueurs des autres zones<br/>
		&nbsp;&nbsp;&nbsp;&nbsp;<br/>
		<h1>Discussions:</h1>
		&nbsp;&nbsp;Les chefs de zones peuvent parler à tous sur le chan local<br/>
		&nbsp;&nbsp;Les joueurs peuvent discuter à tous sur chaque zone avec un délai de 10 sec entre chaque envoi<br/>
		&nbsp;&nbsp;Les joueurs peuvent s'envoyer des mp et des mails<br/>
		&nbsp;&nbsp;Les joueurs peuvent parler à tous sur une planète<br/>
	</p>
